function openWin(u, w, h) 
{ 
      var l = (screen.width - w) / 2; 
      var t = (screen.height - h) / 2; 
      var s = 'width=' + w + ', height=' + h + ', top=' + t + ', left=' + l; 
      s += ', toolbar=no, scrollbars=yes, menubar=no, location=no, resizable=no'; 
      open(u, '', s); 
} 

